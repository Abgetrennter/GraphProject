int strcmp(char *str1, char *str2)
{
    char *p = str1;
    char *q = str2;

    if((NULL == p) || (NULL == q))
    {
        return 0;
    }

    while(*p != '\0' && *q != '\0')
    {
        if(*p == *q)
        {
            p++;
            q++;
        }
        else
        {
            break;
        }
    }

    return (*p - *q);

}

